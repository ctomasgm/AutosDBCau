    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        int carId = Integer.parseInt(tfSearch.getText()); 
        JSONObject json = Ccd.getCarDetail(carId);
        
        if (json != null) {
            car = new CarDetails(json);
            tfAccel.setText(Double.toString(car.getAccel()));
            tfid.setText(Integer.toString(car.getId()));
            tfCylinders.setText(Integer.toString(car.getCylinders()));
            tfEdispl.setText(Integer.toString(car.getEdispl()));
            tfHorsePower.setText(Integer.toString(car.getHorsepower()));
            tfMpg.setText(Double.toString(car.getMpg()));
            tfWeight.setText(Integer.toString(car.getWeight()));
            tfYear.setText(Integer.toString(car.getYear()));
           
            success = new JF_gui_Success();
            success.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            success.setVisible(false);

        } else {
            error = new JF_gui_Error();
            error.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            error.setVisible(false);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

