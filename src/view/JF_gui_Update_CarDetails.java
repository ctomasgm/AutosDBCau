    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        CarDetails carUpdate = new CarDetails(
            Integer.parseInt(tfid.getText()), 
            Double.parseDouble(tfMpg.getText()), 
            Integer.parseInt(tfCylinders.getText()), 
            Integer.parseInt(tfEdispl.getText()), 
            Integer.parseInt(tfHorsePower.getText()), 
            Integer.parseInt(tfWeight.getText()), 
            Double.parseDouble(tfAccel.getText()), 
            Integer.parseInt(tfYear.getText())
        );
        
        if(Ccd.updateCarDetail(carUpdate)) {
            success = new JF_gui_Success();
            success.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            success.setVisible(false);
        } else {
            error = new JF_gui_Error();
            error.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            error.setVisible(false);
        }
        this.dispose();
        
    }//GEN-LAST:event_btnEditActionPerformed

