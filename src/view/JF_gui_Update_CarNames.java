    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        int name = Integer.parseInt(tfSearch.getText()); 
        JSONObject json = Ccd.getCarName(name);
        
        if (json != null) {
            carName = new CarNames(json);
            tfId.setText(Integer.toString(carName.getId()));
            tfModel.setText(carName.getModel());
            tfDescription.setText(carName.getDescr());
           
            success = new JF_gui_Success();
            success.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            success.setVisible(false);

        } else {
            error = new JF_gui_Error();
            error.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            error.setVisible(false);
        }
    }//GEN-LAST:event_btnSearchActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        CarNames carNamesUpdated = new CarNames(
            Integer.parseInt(tfId.getText()), 
            tfModel.getText(),
            tfDescription.getText()
        );
        
        if(Ccd.updateCarName(carNamesUpdated)) {
            success = new JF_gui_Success();
            success.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            success.setVisible(false);
        } else {
            error = new JF_gui_Error();
            error.setVisible(true);
            try {
                Thread.sleep(3000);
            } catch (Exception e) {

            }
            error.setVisible(false);
        }
        this.dispose();
    }//GEN-LAST:event_btnEditActionPerformed

